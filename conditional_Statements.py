a = 5

# IF-ELSE

if a == 5:
    print("a is equal to 5")
else:
    print("a is not equal to 5")

# IF-ELIF-ELSE

if a == 5:
    print("a is equal to 5")
elif a == 6:
    print("a is equal to 6")
else:
    print("a is not equal to 5 or 6")

# Nested IF

if a == 5:
    print("a is equal to 5")
    if a > 0:
        print("a is greater than 0")
    else:
        print("a is not greater than 0")
else:
    print("a is not equal to 5")

# Short Hand IF

if a == 5: print("a is equal to 5")

# Short Hand IF-ELSE

print("a is equal to 5") if a == 5 else print("a is not equal to 5")

