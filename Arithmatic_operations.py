a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))

# Addition
print("Addition: ", a+b)

# Subtraction
print("Subtraction: ", a-b)

# Multiplication
print("Multiplication: ", a*b)

# Division
print("Division: ", a/b)

# Modulus
print("Modulus: ", a%b)

# Exponent
print("Exponent: ", a**b)

# Floor Division
print("Floor Division: ", a//b)

